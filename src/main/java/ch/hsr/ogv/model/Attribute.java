package ch.hsr.ogv.model;

/**
 *
 * @author Adrian Rieser
 * @version OGV 3.1, May 2015
 *
 */
public class Attribute {

	private String name;

	public Attribute() {
	}

	public Attribute(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
